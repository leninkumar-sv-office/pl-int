_instrument_names: Dict[str, str] = {}
_instrument_names_loaded = False
_instrument_names_lock = threading.Lock()


def _headers() -> dict:
    """Build auth headers for Kite API."""
    return {
        "X-Kite-Version": _API_VERSION,
        "Authorization": f"token {_api_key}:{_access_token}",
    }


def _try_auto_login_and_retry(path: str, params: dict = None) -> Optional[dict]:
    """Attempt auto-login and retry the failed request."""
    if not can_auto_login() or _auto_login_in_progress:
        return None
    logger.info("[Zerodha] Attempting auto-login...")
    if auto_login():
        try:
            retry_resp = requests.get(
                f"{_BASE_URL}{path}",
                headers=_headers(),
                params=params,
                timeout=(5, 10),
            )
            if retry_resp.status_code == 200:
                return retry_resp.json()
        except Exception:
            pass
    return None


def _api_get(path: str, params: dict = None) -> Optional[dict]:
    """Make authenticated GET request to Kite API."""
    global _auth_failed, _last_error, _conn_failed, _conn_fail_time
    if not _api_key:
        return None
    # If no token or auth failed, try auto-login before giving up
    if not _access_token or _auth_failed:
        result = _try_auto_login_and_retry(path, params)
        if result is not None:
            return result
        return None
    # Skip if connection recently failed (retry after 10s)
    if _conn_failed and (time.time() - _conn_fail_time) < 10:
        return None
    max_retries = 2
    for attempt in range(max_retries):
        try:
            resp = requests.get(
                f"{_BASE_URL}{path}",
                headers=_headers(),
                params=params,
                timeout=(5, 15),  # (connect_timeout, read_timeout)
            )
            # Connection succeeded — reset conn_failed
            _conn_failed = False
            if resp.status_code == 200:
                _auth_failed = False  # Reset on success
                return resp.json()
            elif resp.status_code == 403:
                _auth_failed = True
                _last_error = f"Auth failed (403): {resp.text[:200]}"
                logger.error(f"[Zerodha] {_last_error}")
                with _lock:
                    global _session_valid
                    _session_valid = False
                # Try auto-login and retry
                result = _try_auto_login_and_retry(path, params)
                if result is not None:
                    return result
                logger.warning("[Zerodha] Token expired. Visit /api/zerodha/login to refresh.")
                return None
            elif resp.status_code == 429:
                # Rate limited — wait and retry
                if attempt < max_retries - 1:
                    time.sleep(1)
                    continue
                _last_error = f"Rate limited (429)"
                logger.warning(f"[Zerodha] {_last_error}")
                return None
            else:
                _last_error = f"API error {resp.status_code}: {resp.text[:100]}"
                logger.error(f"[Zerodha] {_last_error}")
                return None
        except requests.exceptions.RequestException as e:
            if attempt < max_retries - 1:
                time.sleep(0.5)
                continue
            _conn_failed = True
            _conn_fail_time = time.time()
            _last_error = f"Connection failed: {str(e)[:120]}"
            logger.error(f"[Zerodha] Request failed: {e}")
            return None


