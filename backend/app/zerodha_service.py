def is_configured() -> bool:
    """Check if Zerodha API key is configured."""
    return bool(_api_key)


def is_session_valid() -> bool:
    """Check if we have a valid access token (and it hasn't been rejected).
    If session is invalid but auto-login credentials are available, attempts auto-login.
    """
    if _api_key and _access_token and not _auth_failed:
        return True
    # Try auto-login if credentials are available
    if can_auto_login() and not _auto_login_in_progress:
        logger.warning("[Zerodha] Session invalid — attempting auto-login...")
        if auto_login():
            return True
    return False


def get_login_url(redirect_url: str = "http://localhost:9999/api/zerodha/callback") -> str:
    """Get Kite login URL for user to authenticate."""
    return f"{_LOGIN_URL}?v={_API_VERSION}&api_key={_api_key}&redirect_url={redirect_url}"


def generate_session(request_token: str) -> bool:
    """Exchange request_token for access_token using API secret.
    Call this after user completes Kite login."""
    global _access_token, _session_valid, _auth_failed, _conn_failed
    if not _api_key or not _api_secret:
        logger.error("[Zerodha] API key or secret not configured")
        return False

    # Checksum: SHA-256 of (api_key + request_token + api_secret)
    checksum = hashlib.sha256(
        f"{_api_key}{request_token}{_api_secret}".encode()
    ).hexdigest()

    try:
        resp = requests.post(
            f"{_BASE_URL}/session/token",
            data={
                "api_key": _api_key,
                "request_token": request_token,
                "checksum": checksum,
            },
            timeout=10,
        )
        if resp.status_code == 200:
            data = resp.json().get("data", {})
            _access_token = data.get("access_token", "")
            if _access_token:
                # Reset all failure flags — we have a fresh valid token
                _auth_failed = False
                _conn_failed = False
                with _lock:
                    _session_valid = True
                # Persist to .env
                _update_env("ZERODHA_ACCESS_TOKEN", _access_token)
                logger.info(f"[Zerodha] Session created, token: {_access_token[:8]}...")
                return True
        logger.error(f"[Zerodha] Session failed: {resp.status_code} {resp.text[:200]}")
    except Exception as e:
        logger.error(f"[Zerodha] Session error: {e}")
    return False


def set_access_token(token: str):
    """Manually set access token (e.g. from frontend)."""
    global _access_token, _session_valid, _auth_failed, _conn_failed
    _access_token = token.strip()
    _auth_failed = False  # Reset — allow retrying with new token
    _conn_failed = False  # Reset connection failure too
    with _lock:
        _session_valid = bool(_access_token)
    if _access_token:
        _update_env("ZERODHA_ACCESS_TOKEN", _access_token)
        logger.info(f"[Zerodha] Access token set: {_access_token[:8]}...")


def _update_env(key: str, value: str):
    """Update a key in the .env file."""
    try:
        lines = []
        found = False
        if os.path.exists(_ENV_PATH):
            with open(_ENV_PATH) as f:
                for line in f:
                    if line.strip().startswith(f"{key}="):
                        lines.append(f"{key}={value}\n")
                        found = True
                    else:
                        lines.append(line)
        if not found:
            lines.append(f"{key}={value}\n")
        with open(_ENV_PATH, "w") as f:
            f.writelines(lines)
    except Exception as e:
        logger.error(f"[Zerodha] Failed to update .env: {e}")


# ═══════════════════════════════════════════════════════════
#  AUTO-LOGIN (TOTP-based)
# ═══════════════════════════════════════════════════════════

_auto_login_lock = threading.Lock()
_auto_login_in_progress = False


def can_auto_login() -> bool:
    """Check if we have credentials for automated login."""
    return bool(_api_key and _api_secret and _user_id and _password and _totp_secret)


def auto_login() -> bool:
    """Automatically login to Zerodha using stored credentials + TOTP.

    Flow:
      1. POST kite.zerodha.com/api/login with user_id + password → request_id
      2. Generate TOTP code from secret
      3. POST kite.zerodha.com/api/twofa with request_id + totp → session
      4. GET connect/login URL with session cookies → redirect with request_token
      5. Exchange request_token → access_token via generate_session()

    Returns True if new access_token was obtained.
    """
    global _auto_login_in_progress, _access_token, _session_valid, _auth_failed, _conn_failed

    if not can_auto_login():
        logger.warning("[Zerodha] Auto-login not possible — missing credentials")
        return False

    with _auto_login_lock:
        if _auto_login_in_progress:
            return False
        _auto_login_in_progress = True

    try:
        import pyotp
        session = requests.Session()

        # Step 1: Login with user_id + password
        logger.info("[Zerodha] Auto-login: posting credentials...")
        login_resp = session.post(
            "https://kite.zerodha.com/api/login",
            data={"user_id": _user_id, "password": _password},
            timeout=15,
        )
        if login_resp.status_code != 200:
            logger.error(f"[Zerodha] Auto-login: login failed ({login_resp.status_code}): {login_resp.text[:200]}")
            return False

        login_data = login_resp.json()
        request_id = login_data.get("data", {}).get("request_id", "")
        if not request_id:
            logger.error(f"[Zerodha] Auto-login: no request_id in response: {login_data}")
            return False

        # Step 2: Generate TOTP and submit 2FA
        totp = pyotp.TOTP(_totp_secret)
        totp_code = totp.now()
        logger.info("[Zerodha] Auto-login: submitting TOTP...")

        # Use the 2FA type from login response (usually "app_code" for TOTP)
        twofa_type = login_data.get("data", {}).get("twofa_type", "app_code")
        twofa_resp = session.post(
            "https://kite.zerodha.com/api/twofa",
            data={
                "user_id": _user_id,
                "request_id": request_id,
                "twofa_value": totp_code,
                "twofa_type": twofa_type,
            },
            timeout=15,
        )
        if twofa_resp.status_code != 200:
            logger.error(f"[Zerodha] Auto-login: 2FA failed ({twofa_resp.status_code}): {twofa_resp.text[:200]}")
            return False

        # Step 3: Hit the Kite Connect login URL with session cookies
        # Follow redirects manually — stop before hitting our local callback
        # (which may be unreachable inside Docker or on a different port)
        connect_url = f"https://kite.zerodha.com/connect/login?v={_API_VERSION}&api_key={_api_key}"
        logger.info("[Zerodha] Auto-login: fetching connect login for request_token...")

        from urllib.parse import urlparse, parse_qs
        connect_resp = session.get(connect_url, allow_redirects=False, timeout=15)
        request_token = None
        for _ in range(10):  # max redirects
            location = connect_resp.headers.get("Location", "")
            parsed = urlparse(location)
            params = parse_qs(parsed.query)
            if "request_token" in params:
                request_token = params["request_token"][0]
                break
            if not location or connect_resp.status_code not in (301, 302, 303, 307, 308):
                break
            connect_resp = session.get(location, allow_redirects=False, timeout=15)
        # Fallback: check final URL if we followed all redirects
        if not request_token:
            parsed = urlparse(connect_resp.url)
            params = parse_qs(parsed.query)
            request_token = params.get("request_token", [None])[0]

        if not request_token:
            logger.error(f"[Zerodha] Auto-login: request_token not found in redirects: {connect_resp.url[:200]}")
            return False

        # Step 4: Exchange request_token for access_token
        logger.info("[Zerodha] Auto-login: exchanging request_token for access_token...")
        success = generate_session(request_token)
        if success:
            logger.info("[Zerodha] Auto-login: SUCCESS — new access token obtained")
        else:
            logger.error("[Zerodha] Auto-login: token exchange failed")
        return success

    except Exception as e:
        logger.error(f"[Zerodha] Auto-login error: {e}")
        return False
    finally:
        with _auto_login_lock:
            _auto_login_in_progress = False


